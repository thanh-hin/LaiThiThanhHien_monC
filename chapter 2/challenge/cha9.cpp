#include <iostream>
using namespace std;
int main ()//chương trình chính
{
    cout <<"The byte of char: "<<sizeof(char)<<endl;//xuất kích thước của kiểu dữ liệu char
    cout <<"The byte of int: "<<sizeof(int)<<endl;//xuất kích thước của kiểu dữ liệu int
    cout <<"The byte of float: "<<sizeof(float)<<endl;//xuất kích thước của kiểu dữ liệu float
    cout <<"The byte of double: "<<sizeof(double)<<endl;//xuất kích thước của kiểu dữ liệu double
    return 0;
}