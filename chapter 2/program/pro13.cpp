#include <iostream>
using namespace std;
int main ()//chương trình chính
{
    char letter;//khai báo biến kí tự
    //mã ASCII
    letter = 65;//gán giá trị 65 cho biến kí tự
    cout << letter << endl;//hiển thị A
    letter = 66;//gán kí tự 66 cho biến kí tự
    cout << letter << endl;//hiển thị B
    return 0;
}