#include <bits/stdc++.h>
using namespace std;
int main()//chương trình chính
{
    //khai báo biến
    double angle;//góc
    //nhập dodoj của góc hteo radian
    cout <<"Enter an angle: ";
    cin >> angle;
    //hiển thị sin cos tan
    cout <<"Sine of the angle : " << sin(angle) << endl;
    cout <<"Cosine of the angle : " << cos(angle) << endl;
    cout <<"Tangent of the angle : " << tan(angle) << endl;
    return 0;
}