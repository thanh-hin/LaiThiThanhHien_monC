#include <iostream>
using namespace std;
int main()
{
    int number = 0;//khởi tạo giá trị biến
    while (number < 5)//kiểm tra điều kiện
    {
        cout << "Hello\n";//thỏa điều kiện xuất dòng
        number++;// number +1
        //sau đó lên kiểm tra điều kiện 
    }
    cout << "That's all!\n";
    return 0;
}
